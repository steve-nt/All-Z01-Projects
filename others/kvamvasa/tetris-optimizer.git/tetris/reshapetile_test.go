package tetris

import (
	"reflect"
	"testing"
)

func TestReshapeTile(t *testing.T) {
	tests := []struct {
		input [][]string

		want [][]string
	}{
		{
			input: [][]string{
				{".", ".", ".", "."},
				{".", "#", "#", "."},
				{".", "#", "#", "."},
				{".", ".", ".", "."},
			},
			want: [][]string{{"#", "#"}, {"#", "#"}},
		},
		{
			input: [][]string{
				{".", "#", ".", "."},
				{".", "#", "#", "."},
				{".", "#", ".", "."},
				{".", ".", ".", "."},
			},
			want: [][]string{{"#", "."}, {"#", "#"}, {"#", "."}},
		},
		{
			input: [][]string{
				{".", ".", ".", "."},
				{".", ".", "#", "#"},
				{".", "#", "#", "."},
				{".", ".", ".", "."},
			},
			want: [][]string{{".", "#", "#"}, {"#", "#", "."}},
		},
		{
			input: [][]string{
				{".", ".", ".", "."},
				{".", ".", "#", "."},
				{".", "#", "#", "."},
				{".", "#", ".", "."},
			},
			want: [][]string{{".", "#"}, {"#", "#"}, {"#", "."}},
		},
		{
			input: [][]string{
				{".", "#", "#", "#"},
				{".", ".", ".", "#"},
				{".", ".", ".", "."},
				{".", ".", ".", "."},
			},
			want: [][]string{{"#", "#", "#"}, {".", ".", "#"}},
		},
		{
			input: [][]string{
				{"#", "#", ".", "."},
				{".", "#", ".", "."},
				{".", "#", ".", "."},
				{".", ".", ".", "."},
			},
			want: [][]string{{"#", "#"}, {".", "#"}, {".", "#"}},
		},
		{
			input: [][]string{
				{".", "#", ".", "."},
				{".", "#", "#", "."},
				{".", ".", "#", "."},
				{".", ".", ".", "."},
			},
			want: [][]string{{"#", "."}, {"#", "#"}, {".", "#"}},
		},
		{
			input: [][]string{
				{".", ".", ".", "."},
				{"#", "#", "#", "."},
				{".", "#", ".", "."},
				{".", ".", ".", "."},
			},
			want: [][]string{{"#", "#", "#"}, {".", "#", "."}},
		},
		{
			input: [][]string{
				{".", ".", ".", "#"},
				{".", ".", ".", "#"},
				{".", ".", ".", "#"},
				{".", ".", ".", "#"},
			},
			want: [][]string{{"#"}, {"#"}, {"#"}, {"#"}},
		},
		{
			input: [][]string{
				{".", ".", ".", "."},
				{".", ".", ".", "."},
				{".", ".", ".", "."},
				{"#", "#", "#", "#"},
			},
			want: [][]string{{"#", "#", "#", "#"}},
		},
		{
			input: [][]string{
				{".", ".", ".", "."},
				{".", ".", ".", "."},
				{"#", "#", ".", "."},
				{".", "#", "#", "."},
			},
			want: [][]string{{"#", "#", "."}, {".", "#", "#"}},
		},
	}
	for _, test := range tests {
		var coordinates [][]int

		for y, line := range test.input {
			for x, symbol := range line {
				if symbol == "#" {

					coordinates = append(coordinates, []int{x, y})
				}
			}
		}
		if got := reshapeTile(test.input, coordinates); !reflect.DeepEqual(got, test.want) {
			t.Errorf("For %v expected: %v, got %v\n", test.input, test.want, got)
		}
	}
}
