func copyTiles(src [][]int) [][]int {
	out := make([][]int, len(src))
	for r := range src {
		out[r] = append([]int(nil), src[r]...)
	}
	return out
}

// corner spawns (row, col) — walkable corners from original map
var spawnCorners = [4][2]int{
	{1, 1},   // top-left
	{1, 18},  // top-right
	{18, 1},  // bottom-left
	{18, 18}, // bottom-right
}

func inSpawnSafeZone(row, col int) bool {
	for i := 0; i < len(spawnCorners); i++ {
		sr, sc := spawnCorners[i][0], spawnCorners[i][1]
		d := abs(row-sr) + abs(col-sc)
		if d <= 3 {
			return true
		}
	}
	return false
}

func abs(x int) int {
	if x < 0 {
		return -x
	}
	return x
}
