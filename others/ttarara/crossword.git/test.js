// --- ERROR CASES ---
function expectError(name, pzl, wrds) {
  test(name, () => crosswordSolver(pzl, wrds), 'Error')
}

expectError('mismatch words vs slots', '2001\n0..0\n2000\n0..0', ['casa','alan','ciao','anta'])
expectError('start > 2', '0001\n0..0\n3000\n0..0', ['casa','alan','ciao','anta'])
expectError('duplicate word', '2001\n0..0\n1000\n0..0', ['casa','casa','ciao','anta'])
expectError('empty puzzle', '', ['casa','alan','ciao','anta'])
expectError('wrong puzzle type', 123, ['casa','alan','ciao','anta'])
expectError('wrong words type', '2001\n0..0\n1000\n0..0', 123)
expectError('multiple solutions', '2000\n0...\n0...\n0...', ['abba','assa'])
expectError('no solution', '2001\n0..0\n1000\n0..0', ['aaab','aaac','aaad','aaae'])
