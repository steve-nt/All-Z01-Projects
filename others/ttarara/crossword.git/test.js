const crosswordSolver = require('./crosswordSolver.js')


// helper: capture console.log output από τη συνάρτηση
function runAndCapture(fn) {
  const logs = []
  const oldLog = console.log
  console.log = (msg='') => logs.push(String(msg))
  try { fn() } finally { console.log = oldLog }
  return logs.join('\n')
}

function test(name, fn, expected) {
  const out = runAndCapture(fn)
  const ok = out === expected
  const status = ok ? '✓' : '✗'
  console.log(`${status} ${name}`)
  if (!ok) {
    console.log('--- expected ---')
    console.log(expected)
    console.log('---   got    ---')
    console.log(out)
    process.exitCode = 1
  }
}

